package com.springboot.keywords;

/**
 * @author prabhakar, @Date 24-12-2024
 */
public class PrimitiveTypeKeywords {

    //byte: A 1-byte (8-bit) integer data type, storing values from -128 to 127.
    byte a;
    //char: A 2-byte (16-bit) data type for storing a single Unicode character.
    char b;
    //short: A 2-byte (16-bit) integer data type, storing values from -32,768 to 32,767.
    short c;
    //int: A 4-byte (32-bit) integer data type, storing values from -2,147,483,648 to 2,147,483,647.
    int d;
    //long: An 8-byte (64-bit) integer data type, storing values from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807.
    long e;
    //float: A 4-byte (32-bit) floating-point data type, for decimal numbers with single precision.
    float f;
    //double: An 8-byte (64-bit) floating-point data type, for decimal numbers with double precision.
    double g;
    //boolean: A 1-bit data type that stores either true or false.
    boolean h;

    // above keywords all are primitive type data types
    // these are not objects, because of these primitive type
    // java is not 100% object-oriented programing language because of primitive type data types



}
